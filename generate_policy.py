#!/usr/bin/env python3
"""Generates the Policy PDF (Политика обработки ПД) — public document for the website."""

from fpdf import FPDF, XPos, YPos

FONT_PATH = '/Library/Fonts/Arial Unicode.ttf'
OUT_PATH  = '/Users/aleksandrgolubev/Desktop/site_structure/Политика_обработки_ПД_ГРАЙФ_ПЕРМЬ.pdf'

COMPANY_FULL  = 'Общество с ограниченной ответственностью «ГРАЙФ ПЕРМЬ»'
COMPANY_SHORT = 'ООО «ГРАЙФ ПЕРМЬ»'

# Fill these in when the order is signed:
ORDER_DATE   = '«___» _______________ 2026 г.'
ORDER_NUMBER = '___'


class PolicyPDF(FPDF):
    def __init__(self):
        super().__init__(format='A4')
        self.add_font('Arial', '',  FONT_PATH)
        self.add_font('Arial', 'B', FONT_PATH)
        self.set_margins(25, 20, 20)
        self.set_auto_page_break(auto=True, margin=22)
        self._current_section = ''

    def header(self):
        if self.page_no() > 1:
            self.set_font('Arial', '', 9)
            self.set_text_color(120, 120, 120)
            self.cell(0, 8,
                      f'Политика обработки персональных данных в {COMPANY_SHORT}',
                      align='C', new_x=XPos.LMARGIN, new_y=YPos.NEXT)
            self.set_draw_color(180, 180, 180)
            self.set_line_width(0.3)
            self.line(self.l_margin, self.get_y(),
                      self.w - self.r_margin, self.get_y())
            self.ln(3)
            self.set_text_color(0, 0, 0)

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', '', 9)
        self.set_text_color(120, 120, 120)
        self.cell(0, 8, str(self.page_no()), align='C')
        self.set_text_color(0, 0, 0)

    # ── helpers ──────────────────────────────────────────────────────────────

    def par(self, text, size=11, bold=False, align='J', after=4, color=None):
        if color:
            self.set_text_color(*color)
        self.set_font('Arial', 'B' if bold else '', size)
        self.multi_cell(0, 6, text, align=align,
                        new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        if after:
            self.ln(after)
        if color:
            self.set_text_color(0, 0, 0)

    def hline(self, color=(0, 0, 0), thickness=0.4):
        self.set_draw_color(*color)
        self.set_line_width(thickness)
        self.line(self.l_margin, self.get_y(),
                  self.w - self.r_margin, self.get_y())
        self.set_draw_color(0, 0, 0)
        self.ln(3)

    def section_title(self, text):
        self.ln(4)
        self.set_font('Arial', 'B', 11)
        self.set_fill_color(44, 62, 47)
        self.set_text_color(255, 255, 255)
        self.multi_cell(0, 8, '  ' + text, align='L', fill=True,
                        new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        self.set_text_color(0, 0, 0)
        self.set_fill_color(255, 255, 255)
        self.ln(4)

    def clause(self, number, text, size=11):
        num_w = 14
        x0 = self.l_margin
        y0 = self.get_y()
        self.set_font('Arial', 'B', size)
        self.set_xy(x0, y0)
        self.cell(num_w, 6, number)
        self.set_font('Arial', '', size)
        self.set_xy(x0 + num_w, y0)
        self.multi_cell(0, 6, text, align='J',
                        new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        self.ln(3)

    def bullet(self, text, size=11, indent=10):
        bul_w = 6
        x0 = self.l_margin + indent
        y0 = self.get_y()
        self.set_font('Arial', '', size)
        self.set_xy(x0, y0)
        self.cell(bul_w, 6, '-')
        self.set_xy(x0 + bul_w, y0)
        self.multi_cell(0, 6, text, align='J',
                        new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        self.ln(1)

    def subject_block(self, title, items):
        self.set_fill_color(39, 95, 81)
        self.set_text_color(255, 255, 255)
        self.set_font('Arial', 'B', 10)
        self.multi_cell(0, 7, '  ' + title, align='L', fill=True,
                        new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        self.set_text_color(0, 0, 0)
        self.set_fill_color(225, 239, 235)
        x0 = self.l_margin
        for item in items:
            bul_w = 6
            y0 = self.get_y()
            self.set_font('Arial', '', 10)
            self.set_xy(x0 + 4, y0)
            self.cell(bul_w, 5.5, '-')
            self.set_xy(x0 + 10, y0)
            self.multi_cell(0, 5.5, item, align='J',
                            new_x=XPos.LMARGIN, new_y=YPos.NEXT)
        self.set_draw_color(180, 200, 190)
        self.set_line_width(0.3)
        self.ln(4)
        self.set_draw_color(0, 0, 0)
        self.set_fill_color(255, 255, 255)


